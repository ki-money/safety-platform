def get_translation(lang_code, key, default=None):
    """
    Get translation for a specific key in the specified language.
    Falls back to English if translation not found.
    """
    if lang_code not in TRANSLATIONS:
        lang_code = 'English'

    if key in TRANSLATIONS[lang_code]:
        return TRANSLATIONS[lang_code][key]
    elif key in TRANSLATIONS['English']:
        return TRANSLATIONS['English'][key]
    else:
        return default or key


def get_all_translations(lang_code):
    """Get all translations for a specific language."""
    if lang_code not in TRANSLATIONS:
        return TRANSLATIONS['English']
    return TRANSLATIONS[lang_code]


def get_available_languages():
    """Get list of available languages."""
    return list(TRANSLATIONS.keys())